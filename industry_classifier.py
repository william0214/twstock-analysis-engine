    def _build_industry_stocks_map(self) -> Dict[str, List[str]]:
        """
        建立產業到股票代碼的反向映射
        
        Returns:
            Dict[產業代碼, List[股票代碼]]
        """
        industry_map = {}
        for stock_code, (industry_code, _, _) in self.stock_industry_map.items():
            if industry_code not in industry_map:
                industry_map[industry_code] = []
            industry_map[industry_code].append(stock_code)
        return industry_map
    
    def get_stock_industry(self, stock_code: str) -> Optional[Tuple[str, str, str]]:
        """
        獲取股票的產業分類
        
        Args:
            stock_code: 股票代碼
            
        Returns:
            Tuple[產業代碼, 產業名稱, 子產業] 或 None
        """
        return self.stock_industry_map.get(stock_code)
    
    def get_industry_stocks(self, industry_code: str) -> List[str]:
        """
        獲取某產業的所有股票
        
        Args:
            industry_code: 產業代碼
            
        Returns:
            List[股票代碼]
        """
        return self.industry_stocks.get(industry_code, [])
    
    def get_industry_name(self, industry_code: str) -> Optional[str]:
        """
        獲取產業名稱
        
        Args:
            industry_code: 產業代碼
            
        Returns:
            產業名稱或None
        """
        return self.MAJOR_INDUSTRIES.get(industry_code)
    
    def is_electronics_stock(self, stock_code: str) -> bool:
        """
        判斷是否為電子股（產業代碼24-31）
        
        Args:
            stock_code: 股票代碼
            
        Returns:
            bool: 是否為電子股
        """
        industry_info = self.get_stock_industry(stock_code)
        if industry_info:
            industry_code = industry_info[0]
            return industry_code in ['24', '25', '26', '27', '28', '29', '30', '31']
        return False
    
    def is_finance_stock(self, stock_code: str) -> bool:
        """
        判斷是否為金融股
        
        Args:
            stock_code: 股票代碼
            
        Returns:
            bool: 是否為金融股
        """
        industry_info = self.get_stock_industry(stock_code)
        if industry_info:
            return industry_info[0] == '17'
        return False
    
    def get_statistics(self) -> Dict:
        """
        獲取產業分類統計資訊
        
        Returns:
            Dict: 統計資訊
        """
        stats = {
            'total_stocks': len(self.stock_industry_map),
            'total_industries': len(self.industry_stocks),
            'industry_distribution': {}
        }
        
        for industry_code, stocks in self.industry_stocks.items():
            industry_name = self.get_industry_name(industry_code)
            stats['industry_distribution'][industry_name] = {
                'code': industry_code,
                'count': len(stocks),
                'percentage': len(stocks) / len(self.stock_industry_map) * 100
            }
        
        return stats


